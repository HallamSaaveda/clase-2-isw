function convertirMayusculas(cadena) {
    return cadena.toUpperCase();
}
function convertirMinusculas(cadena) {
    return cadena.toLowerCase();
}
function restarNumeros(num1, num2) {
    return num1 - num2;
}
function dividirNumeros(num1, num2) {
    return num2 !== 0 ? num1 / num2 : "no es divisible por 0";
}
function multiplicarNumeros(num1, num2) {
    return num1 * num2;
}
function longitudString(cadena) {
    return cadena.length;
}